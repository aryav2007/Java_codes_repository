package aryav_programs;

public class Datatype {
	public void main(String[] args){
		byte byteNumber=12;
		System.out.println(byteNumber);
		int intNumber=247854785;
		System.out.println(intNumber);
		float fNumber=2.44f;
		System.out.println(fNumber);
		char charAlph=('a');
		System.out.println(charAlph);
		short shortNumber=457;
		System.out.println(shortNumber);
		long longNumber=448454545;
		System.out.println(longNumber);
		double doubleNumber=54525.58714d;
		System.out.println(doubleNumber);
		boolean bool=true;
		System.out.println(bool);	
		System.out.print(byteNumber +" "+ intNumber + " "+ fNumber + " "+charAlph+"  "+ shortNumber+" "+ longNumber+" " + doubleNumber+ " "+ bool);
	}
}
